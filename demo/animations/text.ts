function drawChar(display: VoxelDisplay, char: string, startX: number, startY: number, color: number) {
  const glyph = FONT[char]
  if (!glyph) return
  for (let row = 0; row < 7; row++) {
    for (let col = 0; col < 5; col++) {
      if (glyph[row] & (1 << (4 - col))) {
        display.setPixel(startX + col, startY + (6 - row), color)
      }
    }
  }
}

let getText = (): string => 'HELLO VOXEL '

export function setTextSource(fn: () => string) {
  getText = fn
}

export const text: Animation = {
  name: 'Scrolling Text',
  fn(display, frame) {
    display.clear()
    const str = getText()
    const charWidth = 6
    const totalWidth = str.length * charWidth + display.width
    const offset = frame % totalWidth
    const startY = Math.floor((display.height - 7) / 2)

    for (let i = 0; i < str.length; i++) {
      const x = i * charWidth - offset + display.width
      if (x > -charWidth && x < display.width) {
        const color = (i % 14) + 1
        drawChar(display, str[i], x, startY, color)
      }
    }
  },
}
